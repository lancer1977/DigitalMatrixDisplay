        ['Z'] = new byte[] { 0x61,0x51,0x49,0x45,0x43 },
    };

    public static int MeasureText(string text) => text.Length * 6; // 5px + 1px spacer

    public static void DrawText(IDmdCanvas canvas, string text, int x, int y, DmdColor color, byte intensity = 255)
    {
        int cx = x;

        foreach (var ch0 in text)
        {
            var ch = char.ToUpperInvariant(ch0);
            if (!Glyphs.TryGetValue(ch, out var cols))
                cols = Glyphs[' '];

            DrawChar(canvas, cols, cx, y, color, intensity);
            cx += 6;
        }
    }

    private static void DrawChar(IDmdCanvas canvas, byte[] cols, int x, int y, DmdColor color, byte intensity)
    {
        for (int cx = 0; cx < 5; cx++)
        {
            byte col = cols[cx];
            for (int cy = 0; cy < 7; cy++)
            {
                if (((col >> cy) & 1) == 0) continue;
                canvas.SetPixel(x + cx, y + cy, new DmdPixel(intensity, color), DmdBlendMode.Max);
            }
        }
    }
}
